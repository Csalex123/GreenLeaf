            <p style=" margin:30px;">Reciclar significa transformar objetos materiais usados em novos produtos para o consumo. Esta necessidade foi despertada pelos seres humanos, a partir do momento em que se verificaram os benefícios que este tipo de projeto traz para a nossa Terra. a partir do fim do Séçulo XX a produção de embalagens e produtos descartáveis aumentou significativamente, assim como a produção de lixo, principalmente nos países desenvolvidos. O crescimento econômico deve estar sempre junto com a  preservação do meio ambiente assim como as  Atividades como campanhas de coleta seletiva de lixo e reciclagem de alumínio e papel.
            <br><br>
            O processo de reciclagem, que além de preservar o meio ambiente também gera  fonte de riquezas. Os materiais  reciclados são o vidro, o alumínio, o papel e o plástico. Esta reciclagem contribui para a diminuição da poluição do solo, da água e do ar. Muitas indústrias estão reciclando materiais como uma forma de diminuir os custos de produção. Outra forma  da reciclagem é a quantidade de empregos que ela tem gerado nas grandes cidades. Muitos desempregados estão buscando trabalho neste setor e conseguindo renda para manterem suas famílias. Vários tipos de trabalhadores que trabalham como  catadores de papel e alumínio já são uma boa realidade no Brasil.
            <br><br>
            Muitas campanhas educativas presta bastante  atenção para o problema do lixo nas grandes cidades. Cada vez mais, os centros urbanos, com grande crescimento populacional, têm encontrado dificuldades em conseguir locais para instalarem depósitos de lixo. Portanto, a reciclagem apresenta-se como uma solução totalmente  economica. Nas escolas, muitos alunos são orientados pelos professores a separarem o lixo em suas residências e na própria instituição escolar</p>
