        <h1 align="center">Educação Ambiental &nbsp;&nbsp;<span class="glyphicon glyphicon-duplicate"></span><span class="glyphicon glyphicon-grain"></span></h1>
        <br>

        <p style="margin: 30px";>A educação ambiental é responsável pela comunidade educativa  de sua realidade global, do tipo de relações que os homens estabelecem entre si e com a natureza. Ela desenvolve, uma prática que vincula o educando com a comunidade, valores e atitudes que promovem um comportamento dirigido a transformação superadora dessa realidade, tanto em seus aspectos naturais como sociais.<br><br>

        Ela é  responsável por formar pessoas preocupadas com vários problemas ambientais e que busquem a conservação e preservação dos recursos naturais, abordando os seus aspectos econômicos, sociais, políticos, ecológicos e éticos.. Portanto, falar sobre Educação Ambiental é falar sobre educação acrescentando  a dimensão ambiental adaptada à realidade interdisciplinar aos temas ambientais e globais.<br><br>


        É muito importante para todos, pois as pessoas também fazem parte do meio ambiente. Desde o inicio da história o ser humano sempre viveu de acordo com o ambiente em que vive e também eles tinham que estar sempre conhecendo o ambiente para sobreviver. Nos dias de hoje.. com o avanço da tecnologia ,nós esquecemos  da nossa dependência com o meio ambiente.<br><br>

        Os  problemas causados pelo crescimento populacional, urbanização, industrialização, desmatamento,  aquecimento global, entre outros, obrigaram o mundo a refletir sobre a necessidade de melhorar  a educação ambiental. Esse caso é muito preocupante e deve ser levado a sério, pois as consequências vão atingir a todos.<br><br>

        Esse tipo de educação se busca despertar a preocupação individual e coletiva para a questão ambiental, garantindo o acesso à informação em linguagem adequada, contribuindo para o desenvolvimento  e estimulando o enfrentamento das questões ambientais e sociais procurando trabalhar não apenas a mudança cultural, mas também a transformação social, assumindo a crise ambiental como uma questão ética e política.<br><br><br>

